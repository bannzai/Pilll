/// ピルシートUIで表示されるピルマークの種類を定義するEnum
///
/// ピルの服用状態や種類に応じて異なる視覚表現を提供し、
/// ユーザーがピルシートの状態を一目で把握できるようにする。
/// 各種類は対応するPillMarkコンポーネントで異なる色や形状で表示される。
enum PillMarkType {
  /// 通常の服用対象ピル
  ///
  /// まだ服用していない通常のピルを示す。
  /// UI上ではAppColors.pottiの色で円形表示される。
  normal,

  /// 偽薬期間のピル
  ///
  /// 偽薬（プラセボ）期間に該当するピルを示す。
  /// UI上ではAppColors.blankの色で円形表示される。
  fake,

  /// 休薬期間
  ///
  /// 薬を服用しない休薬期間を示す。
  /// UI上では点線ボーダー付きの空の円形で表示される。
  rest,

  /// 選択状態のピル
  ///
  /// 現在ユーザーが選択している状態のピルを示す。
  /// UI上ではAppColors.enableの色で円形表示される。
  selected,

  /// 服用済みピル
  ///
  /// すでに服用完了したピルを示す。
  /// UI上ではAppColors.lightGrayの色で円形表示され、チェックマーク付きで表示される。
  done,
}
